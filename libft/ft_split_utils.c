#include"libft.h"
#include <stdio.h>

int	ft_utils_split(char const *s, int j)
{
	char	q;

	if ((s[j] == '\"' || s[j] == '\''))
	{
		q = s[j++];
		while (s[j] && s[j] != q)
			j++;
	}
	return (j);
}
